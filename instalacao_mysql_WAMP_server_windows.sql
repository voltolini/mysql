# Instalando o mysql com WAMP no Windows 10.


--> Acessar o site do mySQL:
www.mysql.com


--> No site devemos acessar a "developer zone"
MySQL download

 1. MySQL Workbench (ferramenta para acessar o ambiente)
   1.1 Checar os pré-req
    1.2 escolher a versão desejada
     1.3 versão 64bit com instalador (msi installer) não fazer login



--> Download WAMP Server
1 . www.wampserver.com
 1.1 Download 64 bits
  1.2 WAMP Server irá pedir as configurações do servidor (avançar e finalizar)
   1.3 Irá ficar um 'W' na barra de ferramentas, sempre que o W estiver verde o WAMP  está funcionando.


--> Testar se o MySQL do WAMP está funcionando
1. Abrir a console do WAMP Server
 1.1 aba MySQL
  1.2 Console MySQL
   1.3 MySQL tem um usuário chamado "root" a senha que solicita é uma senha vazia
    1.4 Verificar status do sistema digitar "status"



--> MySQL Workbench (interface gráfica)

1. Ao abrir o Workbench, o mesmo já identificará automaticamente uma instancia local. (localhost:3306 user:root)
 1.1 Ao clicar irá logar no Workbench

